package main

import (
	"fmt"
	"sort"
)

func maximumPopulation(logs [][]int) int {
	lines := make([][]int, 0)
	// sweep lines
	for _, log := range logs {
		birth, death := log[0], log[1]
		lines = append(lines, []int{birth, 1})
		lines = append(lines, []int{death, -1})
	}
	// sort by each point
	sort.SliceStable(lines, func(i, j int) bool {
		if lines[i][0] != lines[j][0] {
			return lines[i][0] < lines[j][0]
		}
		return lines[i][1] < lines[j][1]
	})
	var ans, cnt, maxCnt int
	// loop through line and count max in current interval
	for _, line := range lines {
		cnt += line[1]
		if cnt > maxCnt {
			ans = line[0]
			maxCnt = cnt
		}
	}
	return ans
}
